        // Funzione per ottenere il valore per un determinato "Degree of agreement"
        function getValuesByAgreement(stereotype, degree) {
            return datas.filter(item => item["Behaviours regarding intimate partner"] === stereotype && item["Degree of acceptability"] === degree);
        }

        // Funzione per mappare il colore in base al "Degree of agreement"
        function getColor(degree) {
            switch (degree) {
                case  "always acceptable":
                    return 'rgb(248, 224, 201)'; // Verde
                case "acceptable under certain circumstances":
                    return 'rgb(220, 109, 24)'; // Verde chiaro
                case "never acceptable":
                    return 'rgb(177, 170, 128)'; // Giallo
                case "not answered":
                    return 'rgb(43, 26, 18)'; // Rosso
                default:
                    return 'rgb(169, 169, 169)'; // Grigio per default
            }
        }

        // Funzione per ottenere tutti i valori di stereotipi unici
        function getStereotypeValues() {
            return [...new Set(datas.map(item => item["Behaviours regarding intimate partner"]))];
        }

        // Recuperiamo tutte le regioni
        var territories = [...new Set(datas.map(item => item.Territory))]; // Cambia "data" in "datas"

        // Funzione per creare le checkbox per ogni regione
        function createRegionCheckboxes() {
            var container = document.getElementById('region-checkboxes-violence');
            territories.forEach(territory => {
                var checkbox = document.createElement('input');
                checkbox.type = 'checkbox';
                checkbox.id = territory;
                checkbox.checked = territory === territories[0];  // Imposta come selezionato solo il primo
                checkbox.onchange = updateChart;  // Aggiorna il grafico quando cambiano le selezioni

                var label = document.createElement('label');
                label.setAttribute('for', territory);
                label.innerText = territory;

                var checkboxContainer = document.createElement('div');
                checkboxContainer.classList.add('checkbox-container');
                checkboxContainer.appendChild(checkbox);
                checkboxContainer.appendChild(label);

                container.appendChild(checkboxContainer);
            });
        }

        function updateChart() {
    var selectedStereotype = document.getElementById("stereotype-dropdown-violence").value;

    // Otteniamo le regioni selezionate
    var selectedTerritories = territories.filter(territory => document.getElementById(territory).checked);

    var trace = selectedTerritories.map(territory => {
        var stronglyAgree = getValuesByAgreement(selectedStereotype, "always acceptable").filter(item => item.Territory === territory).map(item => item["Behaviours Value"])[0] || 0;
        var somewhatAgree = getValuesByAgreement(selectedStereotype, "acceptable under certain circumstances").filter(item => item.Territory === territory).map(item => item["Behaviours Value"])[0] || 0;
        var somewhatDisagree = getValuesByAgreement(selectedStereotype, "never acceptable").filter(item => item.Territory === territory).map(item => item["Behaviours Value"])[0] || 0;
        var stronglyDisagree = getValuesByAgreement(selectedStereotype, "not answered").filter(item => item.Territory === territory).map(item => item["Behaviours Value"])[0] || 0;

        return {
            x: [
                "always acceptable",
                "acceptable under certain circumstances",
                "never acceptable",
                "not answered"
            ],
            y: [stronglyAgree, somewhatAgree, somewhatDisagree, stronglyDisagree],
            name: territory,
            type: 'bar',
            marker: {
                color: [
                    getColor("always acceptable"),
                    getColor("acceptable under certain circumstances"),
                    getColor("never acceptable"),
                    getColor("not answered")
                ]
            },
            text: [stronglyAgree, somewhatAgree, somewhatDisagree, stronglyDisagree],
            hovertemplate: '<b>%{x}</b>: %{y}<br>',
            showlegend: false // Disabilita la legenda per le barre
        };
    });

    // Dummy trace per la legenda (solo cerchi neri)
    var legendTrace = selectedTerritories.map(territory => ({
        x: [null], // Non appare nel grafico
        y: [null],
        mode: 'markers',
        marker: {
            color: 'black', // Cerchio nero
            size: 10
        },
        name: territory, // Nome della regione
        type: 'scatter',
        showlegend: true
    }));

    // Combina i trace principali con quelli della legenda
    var data = [...trace, ...legendTrace];

    // Layout del grafico
    var layout = {
        barmode: 'group',
        title: 'Acceptability of intimate partner violence - geographical areas',
        xaxis: {
            title: '"Degree of acceptability"'
        },
        yaxis: {
            title: 'Behaviours Value'
        },
        showlegend: true,
        legend: {
            x: 1, // Posizione della legenda
            y: 1,
            bgcolor: 'rgba(255, 255, 255, 0.5)', // Sfondo semi-trasparente
            bordercolor: 'white',
            borderwidth: 1
        },
        hovermode: 'x unified'
    };

    Plotly.newPlot('chart-container-violence', data, layout);
}

        

        // Carica inizialmente il grafico e le checkbox
        createRegionCheckboxes();
        updateChart();