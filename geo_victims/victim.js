          // Funzione per ottenere i valori in base al "Degree of acceptability"
          function getValuesByAcceptability(region, degree) {
              return data_victims.filter(item => item.Territory === region && item["Degree of acceptability"] === degree);
          }
  
          // Funzione per mappare il colore in base al "Degree of acceptability"
          function getColorVictim(degree) {
              switch (degree) {
                  case "always acceptable": return '#0f2c5c';
                  case "acceptable under certain circumstances": return '#486290';
                  case "never acceptable": return '#8798ba';
                  case "not answered": return '#c8d1e1';
                  default: return 'rgb(169, 169, 169)';
              }
          }
  
          // Recuperiamo tutte le regioni e gli stereotipi unici
          const regions_victim = [...new Set(data_victims.map(item => item.Territory))];
          const stereotypes_victim = [...new Set(data_victims.map(item => item["Behaviours regarding intimate partner"]))];
  
          // Funzione per creare le checkbox per ogni stereotipo
          function createVictimCheckboxes() {
              const container = document.getElementById('stereotype-checkboxes-victims');
              stereotypes_victim.forEach((stereotype, index) => {
                  const checkbox = document.createElement('input');
                  checkbox.type = 'checkbox';
                  checkbox.id = stereotype;
                  checkbox.checked = index === 0; // Seleziona solo il primo stereotipo
                  checkbox.onchange = updateChartVictims; // Aggiorna il grafico quando cambiano le selezioni
  
                  const label = document.createElement('label');
                  label.setAttribute('for', stereotype);
                  label.innerText = stereotype;
  
                  const checkboxContainer = document.createElement('div');
                  checkboxContainer.classList.add('checkbox-container-victims');
                  checkboxContainer.appendChild(checkbox);
                  checkboxContainer.appendChild(label);
  
                  container.appendChild(checkboxContainer);
              });
          }
  
          // Funzione per creare il dropdown delle regioni
          function createRegionVictimsDropdown() {
              const dropdown = document.getElementById('region-dropdown-victims');
              regions_victim.forEach(region => {
                  const option = document.createElement('option');
                  option.value = region;
                  option.textContent = region;
                  dropdown.appendChild(option);
              });
          }
  
 // Funzione per aggiornare il grafico in base alla selezione
function updateChartVictims() {
  const selectedRegion = document.getElementById("region-dropdown-victims").value;

  const selectedStereotypes = stereotypes_victim.filter(stereotype => 
      document.getElementById(stereotype).checked
  );

  const traces = selectedStereotypes.map(stereotype => {
      const alwaysAcceptable = getValuesByAcceptability(selectedRegion, "always acceptable")
          .filter(item => item["Behaviours regarding intimate partner"] === stereotype)
          .map(item => item["Behaviours Value"])[0] || 0;

      const acceptableUnderCertain = getValuesByAcceptability(selectedRegion, "acceptable under certain circumstances")
          .filter(item => item["Behaviours regarding intimate partner"] === stereotype)
          .map(item => item["Behaviours Value"])[0] || 0;

      const neverAcceptable = getValuesByAcceptability(selectedRegion, "never acceptable")
          .filter(item => item["Behaviours regarding intimate partner"] === stereotype)
          .map(item => item["Behaviours Value"])[0] || 0;

      const notAnswered = getValuesByAcceptability(selectedRegion, "not answered")
          .filter(item => item["Behaviours regarding intimate partner"] === stereotype)
          .map(item => item["Behaviours Value"])[0] || 0;

      return {
          x: [
              wrapText("always acceptable", 15),
              wrapText("acceptable under certain circumstances", 15),
              wrapText("never acceptable", 15),
              wrapText("not answered", 15)
          ],
          y: [alwaysAcceptable, acceptableUnderCertain, neverAcceptable, notAnswered],
          name: stereotype,
          type: 'bar',
          marker: {
              color: [
                  getColorVictim("always acceptable"),
                  getColorVictim("acceptable under certain circumstances"),
                  getColorVictim("never acceptable"),
                  getColorVictim("not answered")
              ]
          },
          hovertemplate: 
              'Behavior: ' + stereotype + '<br>' + 
              'Value: <b>%{y}</b><extra></extra>' // Aggiungi il nome dello stereotipo e il valore
      };
  });

  const layout = {
      barmode: 'group',
      xaxis: { 
          title: 'Degree of Acceptability',
          tickangle: 0,  // Le etichette dell'asse X rimarranno orizzontali
          automargin: true,  // Aggiungi margini automatici per evitare sovrapposizioni
          tickfont: {
              size: 12,
              family: 'Arial, sans-serif'
          }
      },
      yaxis: { 
          title: 'Values',
          automargin: true  // Aggiungi margini automatici per evitare sovrapposizioni
      },
      showlegend: false,
      hovermode: 'x unified',
     
  };

  Plotly.newPlot('chart-container-victims', traces, layout);
  // Forza un ridimensionamento del grafico al caricamento iniziale
  Plotly.Plots.resize(document.getElementById('chart-container-victims'));
}

// Funzione per dividere il testo lungo in due righe se è troppo lungo
function wrapText(text, maxLength) {
  if (text.length > maxLength) {
      var midpoint = Math.floor(text.length / 2);
      return text.substring(0, midpoint) + '<br>' + text.substring(midpoint);
  }
  return text;
}


  
